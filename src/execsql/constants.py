from __future__ import annotations

"""
Static constants for the execsql map-display subsystem.

Contains:

- ``bm_servers`` — tile-server URL templates for map basemap layers
  (OpenStreetMap, Google, etc.).
- ``icon_xbm`` — X11 XBM bitmap strings for Tkinter map pin icons (ball,
  bird, flag, star, …).
- ``expand_xbm``, ``wedges_3_xbm``, ``wedge_sm_xbm``, ``cancel_xbm`` —
  button-bar icon bitmaps.
- ``color_names`` — tuple of all CSS/X11 color names accepted for map
  symbol colours.
- ``custom_icons`` — mutable dict populated at runtime with user-defined
  colour+icon combinations.
"""

